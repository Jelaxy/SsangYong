/*
 공유, 통합, 저장, 운영 데이터의 4가지로 나뉜다.
 
 - 공유데이터 : 특정 조직의 여러 사용자가 함께 소유하고 이용할 수 있는 공용데이터
	ex) 특정 조직 : 상대적 개념 해당 시스템을 사용하는 사용자로 범위가 넓을 수도 시스템에 따라 좁을 수도 있고,
