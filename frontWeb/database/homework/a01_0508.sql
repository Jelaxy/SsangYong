	사원정보와 부서정보는 다른 저장 공간에 개별적을 저장되어있을 수 있다.
	이 때, 특정 사원이 해당 사원에 소속된 부서정보를 검색하려면 통합적인 처리를 해야한다.
- 저장데이터 : 컴퓨터가 접근할 수 있는 매체에 저장된 데이터
	ex) 물리적으로 저장할 수 있는 저장 매체(ssd, hdd, 마그넥테이블 등 여러가지 지속적으로 저장할 수 있는 매체)에
	하드웨어적으로 저장했다가 필요에 따라서 가져와서 프로그램에서 호출하여 사용할 수 있다는 것을 말한다.
- 운영데이터 : 조직의 주요 기능을 수행하기 위해 지속적으로 꼭 필요한 데이터
	ex) 은행 업무에서 고객이 대출을 하거나 상환을 하는 등 업무적인 처리를 하고 그 내용이 반영된 데이터로 운영에 꼭 필요로 한다.
			
